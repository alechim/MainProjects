# Python Exercise 2 - Datatypes

# Default Type Int
calculation = 5 + 6

# Type set to float
decimal = 1.5

# Type set to string
word = "Alec"

# Type set to boolean
bool = False

# type(parameter) -> returns the datatype of the parameter
print(type(decimal))
print(type(calculation))
print(type(word))
print(type(bool))

# <variable>.lower() -> converts string to lowercase
print(word.lower())

# <variable>.upper() -> converts string to uppercase
print(word.upper())